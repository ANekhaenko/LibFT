#include "libft.h"

int		ft_isalnum(int i)
{
	if (i >= '0' && i <= '9')
		return (1);
	if (i >= 'A' && i <= 'Z')
		return (1);
	if (i >= 'a' && i <= 'z')
		return (1);
	return (0);
}
